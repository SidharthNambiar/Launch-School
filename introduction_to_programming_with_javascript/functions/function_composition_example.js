// Introduction to Programming with JavaScript
// Functions


function add(a, b) {
    return a + b;
  }
  
function subtract(a, b) {
    return a - b;
}

function times(num1, num2) {
    return num1 * num2;
  }
  
let sum = add(20, 45);
console.log(sum);               // => 65

let difference = subtract(80, 10);
console.log(difference);        // => 70
console.log(add(20, 45));       // => 65
console.log(subtract(80, 10));  // => 70